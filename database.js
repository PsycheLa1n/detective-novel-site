const initSqlJs = require('sql.js');
const fs = require('fs');
const path = require('path');

const DB_PATH = path.join(__dirname, 'database.sqlite');

let db;

async function getDb() {
  if (db) return db;
  const SQL = await initSqlJs();

  let needsRecreate = false;
  if (fs.existsSync(DB_PATH)) {
    const buffer = fs.readFileSync(DB_PATH);
    db = new SQL.Database(buffer);
    // Check if database is up-to-date (119+ books = new schema)
    try {
      const result = db.exec("SELECT COUNT(*) as cnt FROM books");
      const count = result[0].values[0][0];
      if (count < 100) {
        console.log(`旧数据库仅有 ${count} 本书，正在自动升级...`);
        needsRecreate = true;
        db.close();
        fs.unlinkSync(DB_PATH);
      }
    } catch (e) {
      console.log('数据库 schema 不兼容，正在自动重建...');
      needsRecreate = true;
      try { db.close(); } catch (_) {}
      if (fs.existsSync(DB_PATH)) fs.unlinkSync(DB_PATH);
    }
  }

  if (!db || needsRecreate) {
    db = new SQL.Database();
    createTables();
    seedData();
    saveDb();
    console.log('数据库初始化完成：119 本推理小说，33 个标签');
  }

  return db;
}

function saveDb() {
  const data = db.export();
  const buffer = Buffer.from(data);
  fs.writeFileSync(DB_PATH, buffer);
}

function createTables() {
  db.run(`
    CREATE TABLE IF NOT EXISTS books (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      title TEXT NOT NULL,
      author TEXT NOT NULL,
      year INTEGER NOT NULL,
      description TEXT,
      cover_color TEXT,
      rating REAL DEFAULT 0,
      created_at DATETIME DEFAULT CURRENT_TIMESTAMP
    )
  `);
  db.run(`
    CREATE TABLE IF NOT EXISTS tags (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      name TEXT NOT NULL UNIQUE
    )
  `);
  db.run(`
    CREATE TABLE IF NOT EXISTS book_tags (
      book_id INTEGER NOT NULL,
      tag_id INTEGER NOT NULL,
      PRIMARY KEY (book_id, tag_id),
      FOREIGN KEY (book_id) REFERENCES books(id),
      FOREIGN KEY (tag_id) REFERENCES tags(id)
    )
  `);
  db.run(`
    CREATE TABLE IF NOT EXISTS users (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      username TEXT NOT NULL UNIQUE,
      password TEXT NOT NULL,
      created_at DATETIME DEFAULT CURRENT_TIMESTAMP
    )
  `);
  db.run(`
    CREATE TABLE IF NOT EXISTS messages (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      user_id INTEGER NOT NULL,
      content TEXT NOT NULL,
      created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
      FOREIGN KEY (user_id) REFERENCES users(id)
    )
  `);
}

